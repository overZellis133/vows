export function getQuoteById(id: string): Quote | undefined {
  return philosopherQuotes.find(quote => quote.id === id);
}

export function getQuotesByCategory(category: string): Quote[] {
  return philosopherQuotes.filter(quote => quote.category === category);
}

export function getAllCategories(): string[] {
  const categories = new Set(philosopherQuotes.map(q => q.category).filter((cat): cat is string => Boolean(cat)));
  return Array.from(categories);
}

export function getQuotesByAuthor(author: string): Quote[] {
  return philosopherQuotes.filter(quote => quote.author === author);
}

export function getAllAuthors(): string[] {
  const authors = new Set(philosopherQuotes.map(q => q.author));
  return Array.from(authors).sort();
}
